public class ThueMorse{
    public static void main(String[] args){
        String seed = "0" + "1";
        System.out.println(seed);
        seed += "10";
        System.out.println(seed);
        seed += "1001";
        System.out.println(seed);
        seed += "10010110";
        System.out.println(seed);
        seed += "1001011001101001";
        System.out.println(seed);
        seed += "10010110011010010110100110010110";
        System.out.println(seed);

    }
}
